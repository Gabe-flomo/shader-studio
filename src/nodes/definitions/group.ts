    'Use the "Group nodes" action to create one from a selection.',

  // Populated dynamically per instance — the static def has no sockets.
  inputs: {},
  outputs: {},

  defaultParams: { iterations: 1 },
  paramDefs: {
    iterations: { label: 'Iterations', type: 'float', min: 1, max: 16, step: 1 },
  },

  // Stub — real code is generated by graphCompiler when it encounters type === 'group'
  generateGLSL: () => ({ code: '', outputVars: {} }),
